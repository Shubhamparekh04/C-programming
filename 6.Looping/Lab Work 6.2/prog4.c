/*
Write a Program to print the Fibonacci series up to N numbers using a for loop.
For example,
Input:
Enter any number: 8
Output:
0, 1, 1, 2, 3, 5, 8, 13
*/

#include<stdio.h>
void main(){
    int a=0,b=1,c,n;
    printf("Enter number : ");
    scanf("%d",&n);

    for(int i=1;i<=n;i++){
        printf("%d ",a);
        c=a+b;
        a=b;
        b=c;
    }
}